import Foundation

enum RealtimeSemanticEagerness: String {
    case low
    case medium
    case high
    case automatic = "auto"
}

enum RealtimeVoiceActivityDetection {
    case semantic(eagerness: RealtimeSemanticEagerness = .automatic)
    case server(
        threshold: Double = 0.5,
        prefixPaddingMilliseconds: Int = 300,
        silenceDurationMilliseconds: Int = 500
    )

    fileprivate var eventObject: [String: Any] {
        switch self {
        case .semantic(let eagerness):
            return [
                "type": "semantic_vad",
                "eagerness": eagerness.rawValue,
                "create_response": true,
                "interrupt_response": true,
            ]
        case .server(let threshold, let prefixPadding, let silenceDuration):
            return [
                "type": "server_vad",
                "threshold": min(1, max(0, threshold)),
                "prefix_padding_ms": max(0, prefixPadding),
                "silence_duration_ms": max(100, silenceDuration),
                "create_response": true,
                "interrupt_response": true,
            ]
        }
    }
}

/// Aurora protects the beginning of each spoken turn with explicit server-side
/// prefix audio. Semantic VAD has no supported prefix-padding control, which
/// made a quiet first word vulnerable to being omitted from the committed turn.
enum AuroraVoiceActivityProfile {
    static let live = RealtimeVoiceActivityDetection.server(
        threshold: 0.5,
        prefixPaddingMilliseconds: 600,
        silenceDurationMilliseconds: 500
    )
}

struct RealtimeSessionConfiguration {
    let apiKey: String
    let instructions: String
    let toolsJSON: String
    let vad: RealtimeVoiceActivityDetection

    init(
        apiKey: String,
        instructions: String,
        toolsJSON: String = "[]",
        vad: RealtimeVoiceActivityDetection = AuroraVoiceActivityProfile.live
    ) {
        self.apiKey = apiKey
        self.instructions = instructions
        self.toolsJSON = toolsJSON
        self.vad = vad
    }
}

struct RealtimeFunctionCall {
    let connectionID: UUID
    let responseID: String
    /// The committed user audio item that caused this response, when one
    /// exists. Tool continuations retain the originating item across responses.
    let inputItemID: String?
    let callID: String
    let name: String
    /// True only for the continuation created after a temporary native screen
    /// image was acknowledged. The tool layer uses this to ensure untrusted
    /// pixels can request at most the snapshot-bound visual click.
    let visualContextBound: Bool
    /// True for continuations produced from email/provider content. The native
    /// tool boundary prevents that untrusted data from chaining into computer,
    /// memory, relationship, or visual capabilities.
    let untrustedMailContextBound: Bool
    /// True only when asynchronous transcription failed and a context-isolated,
    /// audio-item-only classifier exactly matched the first bounded native call.
    let audioCorroborated: Bool
    /// True when this owner turn has already produced audible Aurora speech.
    /// A successful, visibly self-evident action can then finish without a
    /// redundant second confirmation response.
    let turnAlreadySpoke: Bool
    /// Native causal provenance. This is not supplied by Realtime function
    /// arguments and prevents a continuation from borrowing an earlier turn's
    /// authorization merely because it carries the same input item ID.
    let authorizationSource: ToolAuthorizationSource
    /// Host-derived terminal state of the source audio transcript boundary.
    /// This survives a Realtime transport replacement so a finalized direct
    /// owner handoff cannot be mistaken for an uncommitted call merely because
    /// per-connection transcript caches were cleared.
    let sourceTurnFinalized: Bool

    /// The exact JSON argument string generated by the model.
    let argumentsJSON: String
    /// Exact task-effect binding captured on the original owner-audio helper
    /// call, present only when a later delegate_task matches it.
    let preauthorizedDelegateBinding: String?

    init(
        connectionID: UUID,
        responseID: String,
        inputItemID: String?,
        callID: String,
        name: String,
        visualContextBound: Bool,
        untrustedMailContextBound: Bool,
        audioCorroborated: Bool = false,
        turnAlreadySpoke: Bool,
        authorizationSource: ToolAuthorizationSource = .directOwnerTurn,
        sourceTurnFinalized: Bool,
        argumentsJSON: String,
        preauthorizedDelegateBinding: String? = nil
    ) {
        self.connectionID = connectionID
        self.responseID = responseID
        self.inputItemID = inputItemID
        self.callID = callID
        self.name = name
        self.visualContextBound = visualContextBound
        self.untrustedMailContextBound = untrustedMailContextBound
        self.audioCorroborated = audioCorroborated
        self.turnAlreadySpoke = turnAlreadySpoke
        self.authorizationSource = authorizationSource
        self.sourceTurnFinalized = sourceTurnFinalized
        self.argumentsJSON = argumentsJSON
        self.preauthorizedDelegateBinding = preauthorizedDelegateBinding
    }

    func decodedArguments() throws -> Any {
        guard let data = argumentsJSON.data(using: .utf8) else {
            throw AuroraRealtimeError.invalidFunctionArguments(name: name)
        }
        return try JSONSerialization.jsonObject(with: data)
    }
}

