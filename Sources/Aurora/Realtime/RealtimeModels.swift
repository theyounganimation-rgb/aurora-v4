enum RealtimeToolContinuation: Equatable {
    case speak
    /// A validated, host-authored conversational decision is ready. The next
    /// response may speak, but may not select another tool or silently replace
    /// that decision with an assistant-style default.
    case conversationMove
    case delegateAccepted
    /// Realtime produced an invalid structured semantic proposal. Correct the
    /// structure once against the original finalized owner turn without
    /// speaking or widening the authorized effect.
    case delegateRetry
    /// Realtime truthfully classified the turn as a different semantic domain
    /// while calling conversation_move. Retry once with only the correct
    /// semantic function exposed; the host still never reparses owner words.
    case semanticRouteRetry(toolName: String)
    case silent
    case complete
}

enum AuroraRealtimeError: LocalizedError {
    case missingAPIKey
    case invalidToolsJSON
    case toolsMustBeJSONArray
    case invalidFunctionArguments(name: String)
    case unknownFunctionCall
    case noPreviousConfiguration
    case notConnected
    case malformedServerMessage
    case outboundBackpressure
    case server(code: String?, message: String)
    case transport(Error)
    case audio(Error)

    var errorDescription: String? {
        switch self {
        case .missingAPIKey:
            return "Aurora needs an OpenAI API key before voice can begin."
        case .invalidToolsJSON:
            return "Aurora's voice tools are not valid JSON."
        case .toolsMustBeJSONArray:
            return "Aurora's voice tools must be a JSON array."
        case .invalidFunctionArguments(let name):
            return "Aurora received invalid arguments for the \(name) tool."
        case .unknownFunctionCall:
            return "Aurora received a result for a tool call that is no longer active."
        case .noPreviousConfiguration:
            return "Aurora has no previous voice session to reconnect."
        case .notConnected:
            return "Aurora's voice session is not connected."
        case .malformedServerMessage:
            return "Aurora received an unreadable Realtime event."
        case .outboundBackpressure:
            return "Aurora's voice connection fell too far behind live speech."
        case .server(let code, let message):
            if let code, !code.isEmpty {
                return "Realtime error \(code): \(message)"
            }
            return "Realtime error: \(message)"
        case .transport(let error):
            return "Aurora's voice connection ended: \(error.localizedDescription)"
        case .audio(let error):
            return "Aurora's audio path failed: \(error.localizedDescription)"
        }
    }

    /// Copy safe to place directly in the voice-first interface. Detailed API
    /// and operating-system messages remain available through
    /// `localizedDescription` for the private diagnostic journal.
    var userFacingDescription: String {
        switch self {
        case .server:
            return "Aurora couldn't reach the voice service just now."
        case .transport:
            return "Aurora lost the voice connection."
        case .audio:
            return "Aurora's audio path stopped unexpectedly."
        default:
            return errorDescription ?? "Aurora couldn't continue just now."
        }
    }
}

extension RealtimeSessionConfiguration {
    func validatedTools() throws -> [[String: Any]] {
        let trimmedKey = apiKey.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmedKey.isEmpty else { throw AuroraRealtimeError.missingAPIKey }
        guard let data = toolsJSON.data(using: .utf8) else {
            throw AuroraRealtimeError.invalidToolsJSON
        }

        let object: Any
        do {
            object = try JSONSerialization.jsonObject(with: data)
        } catch {
            throw AuroraRealtimeError.invalidToolsJSON
        }
        guard let tools = object as? [[String: Any]] else {
            throw AuroraRealtimeError.toolsMustBeJSONArray
        }
        return tools
    }

    func sessionUpdate(tools: [[String: Any]]) -> [String: Any] {
        [
            "type": "session.update",
            "session": [
                "type": "realtime",
                "model": AuroraRealtimeClient.model,
                "output_modalities": ["audio"],
                // Realtime otherwise defaults to an unbounded per-response
                // allowance. This ceiling prevents runaway output while the
                // voice contract—not a tiny hard cap—keeps ordinary turns
                // brief. Longer requested explanations can therefore finish.
                "max_output_tokens": AuroraRealtimeClient.maxResponseOutputTokens,
                "instructions": instructions,
                "truncation": [
                    "type": "retention_ratio",
                    "retention_ratio": AuroraRealtimeClient.conversationRetentionRatio,
                    "token_limits": [
                        "post_instructions": AuroraRealtimeClient.postInstructionConversationTokenLimit,
                    ],
                ],
                "audio": [
                    "input": [
                        "format": [
                            "type": "audio/pcm",
                            "rate": Int(AuroraAudioEngine.sampleRate),
                        ],
                        "noise_reduction": ["type": "near_field"],
                        "transcription": ["model": "gpt-4o-mini-transcribe"],
                        "turn_detection": vad.eventObject,
                    ],
                    "output": [
                        "format": [
                            "type": "audio/pcm",
                            "rate": Int(AuroraAudioEngine.sampleRate),
                        ],
                        "voice": AuroraRealtimeClient.voice,
                    ],
                ],
                "tools": tools,
                // Every owner-audio turn first resolves into an exact Codex
                // task proposal, a bounded conversation_move, or an explicit
                // silent terminal. Only a validated continuation may speak.
                "tool_choice": "required",
                "reasoning": [
                    // High effort materially improves adherence to Aurora's
                    // compact social and self-knowledge boundaries without a
                    // measurable latency penalty in the live probe.
                    "effort": "high",
                ],
            ],
        ]
    }
}
